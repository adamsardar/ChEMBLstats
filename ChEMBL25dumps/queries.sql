
/* ChEMBLid2SMILES.tsv*/
SELECT CANONICAL_SMILES, CHEMBL_ID, MOLREGNO FROM CHEMBL_25.COMPOUND_STRUCTURES
    JOIN CHEMBL_25.CHEMBL_ID_LOOKUP ON MOLREGNO = ENTITY_ID
    WHERE ENTITY_TYPE = 'COMPOUND' AND STATUS = 'ACTIVE';

/* ChEMBL CPI data: ChEMBLstandardisedBioactivity.tsv */
SELECT CHEMBL_25.ACTIVITIES.MOLREGNO,
       CHEMBL_25.CHEMBL_ID_LOOKUP.CHEMBL_ID,
       CHEMBL_25.ACTIVITIES.STANDARD_RELATION,
       CHEMBL_25.ACTIVITIES.STANDARD_VALUE,
       CHEMBL_25.ACTIVITIES.STANDARD_UNITS,
       CHEMBL_25.ACTIVITIES.STANDARD_TYPE,
       CHEMBL_25.ASSAYS.TID,
       CHEMBL_25.TARGET_DICTIONARY.PREF_NAME,
       CHEMBL_25.TARGET_DICTIONARY.ORGANISM
    FROM CHEMBL_25.ACTIVITIES
    JOIN CHEMBL_25.ASSAYS ON ASSAYS.ASSAY_ID = ACTIVITIES.ASSAY_ID
    JOIN CHEMBL_25.TARGET_DICTIONARY ON ASSAYS.TID = TARGET_DICTIONARY.TID
    JOIN CHEMBL_25.CHEMBL_ID_LOOKUP ON  CHEMBL_25.ACTIVITIES.MOLREGNO = CHEMBL_25.CHEMBL_ID_LOOKUP.ENTITY_ID
    WHERE STANDARD_FLAG = 1 AND
          TARGET_DICTIONARY.TARGET_TYPE = 'SINGLE PROTEIN' AND
          CHEMBL_25.CHEMBL_ID_LOOKUP.ENTITY_TYPE = 'COMPOUND';


/* Clinically approved status*/
SELECT MOLREGNO, CHEMBL_ID, PREF_NAME, THERAPEUTIC_FLAG, MAX_PHASE, MOLECULE_TYPE,FIRST_APPROVAL
       FROM CHEMBL_25.MOLECULE_DICTIONARY;

